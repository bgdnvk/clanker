// handleTencentExpiry walks every PREPAID-capable resource across the
// requested regions and returns items at or below the renewal threshold.
// Query params: ?regions=ap-x,ap-y &threshold=14 &manual_only=true &include_ssl=false.
// regions defaults to the server's configured region; threshold defaults
// to 30; manual_only defaults to true; include_ssl defaults to false.
//
// Every requested region is validated through the same regex SendRaw uses,
// so SSRF-shaped strings are rejected with 400 before any SDK call fires.
func (s *Server) handleTencentExpiry(w http.ResponseWriter, r *http.Request) {
	client, err := s.tencentClient(r)
	if err != nil {
		writeTencentClientErr(w, err)
		return
	}

	q := r.URL.Query()
	var regions []string
	if raw := strings.TrimSpace(q.Get("regions")); raw != "" {
		for _, part := range strings.Split(raw, ",") {
			region := strings.TrimSpace(part)
			if region == "" {
				continue
			}
			if err := validateRegion(region); err != nil {
				writeError(w, http.StatusBadRequest, "invalid_region", err.Error())
				return
			}
			regions = append(regions, region)
		}
	}

	threshold := 30
	if v := strings.TrimSpace(q.Get("threshold")); v != "" {
		n, err := strconv.Atoi(v)
		if err != nil || n < 0 {
			writeError(w, http.StatusBadRequest, "invalid_threshold", "threshold must be a non-negative integer")
			return
		}
		threshold = n
	}

	manualOnly := true
	if v := strings.TrimSpace(q.Get("manual_only")); v != "" {
		b, err := strconv.ParseBool(v)
		if err != nil {
			writeError(w, http.StatusBadRequest, "invalid_manual_only", "manual_only must be true or false")
			return
		}
		manualOnly = b
	}

	includeSSL := false
	if v := strings.TrimSpace(q.Get("include_ssl")); v != "" {
		b, err := strconv.ParseBool(v)
		if err != nil {
			writeError(w, http.StatusBadRequest, "invalid_include_ssl", "include_ssl must be true or false")
			return
		}
		includeSSL = b
	}

	report, err := client.BuildExpiryReport(r.Context(), tencent.ExpiryReportOptions{
		Regions:       regions,
		ThresholdDays: threshold,
		ManualOnly:    manualOnly,
		IncludeSSL:    includeSSL,
	})
	if err != nil {
		writeError(w, http.StatusBadGateway, "tencent_api_error", err.Error())
		return
	}
	writeData(w, report)
}

// handleTencentCostVoucherUsage returns the deduction history for one voucher.
func (s *Server) handleTencentCostVoucherUsage(w http.ResponseWriter, r *http.Request) {
	voucherID := strings.TrimSpace(r.PathValue("voucher_id"))
	if voucherID == "" {
		writeError(w, http.StatusBadRequest, "missing_param", "voucher_id is required")
		return
	}
	client, err := s.tencentClient(r)
	if err != nil {
		writeTencentClientErr(w, err)
		return
	}
	body, err := client.VoucherUsageJSON(r.Context(), voucherID)
	if err != nil {
		writeError(w, http.StatusBadGateway, "tencent_api_error", err.Error())
		return
	}
	writeRawData(w, body)
}
