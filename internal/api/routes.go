func (s *Server) handleTencentResources(w http.ResponseWriter, r *http.Request) {
	resourceType := strings.ToLower(strings.TrimSpace(r.PathValue("type")))
	if resourceType == "" {
		writeError(w, http.StatusBadRequest, "missing_param", "resource type is required")
		return
	}

	client, err := s.tencentClient(r)
	if err != nil {
		writeTencentClientErr(w, err)
		return
	}

	// We reuse context.go's JSON gather funcs since they already emit the
	// shape we want to return. Each function targets the client's current
	// region; multi-region fan-out for HTTP is a future enhancement.
	ctx, cancel := context.WithTimeout(r.Context(), 60*time.Second)
	defer cancel()

	// Call the typed gather for the requested resource type directly.
	// Earlier revisions also invoked GetRelevantContext (the multi-section
	// LLM gather) before this and discarded the result — doubling every
	// SDK call. The HTTP endpoint only needs the one typed section.
	body, err := gatherTencentByType(ctx, client, resourceType)
	if err != nil {
		// Service not enabled or unsupported — return an empty list with
		// the underlying reason so the dashboard can show "no items + reason".
		writeJSON(w, http.StatusOK, map[string]interface{}{
			"data":    []interface{}{},
			"warning": err.Error(),
		})
		return
	}
	if strings.TrimSpace(body) == "" {
		writeData(w, []interface{}{})
		return
	}
	writeRawData(w, body)
}

